function stop(){
    document.querySelectorAll('audio').forEach(el => el.pause());
    document.querySelectorAll('audio').forEach(el => el.currentTime = 0);
}

function playa(){
    let audioa = document.getElementById("aa");

    document.querySelectorAll('audio').forEach(el => el.pause());

    audioa.currentTime = 0;
    audioa.play();
}

